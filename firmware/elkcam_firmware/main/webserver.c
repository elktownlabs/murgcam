/* An HTTP GET handler */
static esp_err_t photo_get_handler(httpd_req_t *req)
{
    char*  buf;
    size_t buf_len;

    /* Get header value string length and allocate memory for length + 1,
     * extra byte for null termination */
    buf_len = httpd_req_get_hdr_value_len(req, "Host") + 1;
    if (buf_len > 1) {
        buf = malloc(buf_len);
        /* Copy null terminated value string into buffer */
        if (httpd_req_get_hdr_value_str(req, "Host", buf, buf_len) == ESP_OK) {
            ESP_LOGI(TAG, "Host %s requested a picture.", buf);
        }
        free(buf);
    }


    uint8_t *cam_buf = NULL;

    ESP_LOGI(TAG, "Taking picture");
    reinit_cam();
    camhandler_load_config_from_nvs();
    cam_start();
    size_t data_len = cam_take(&cam_buf, 8000 / portTICK_PERIOD_MS);
    cam_stop();
    if (data_len == 0) {
        ESP_LOGE(TAG, "Camera timed out");
        cam_give(cam_buf);
        return ESP_OK;
    }
    /* find end of jpeg stream and truncate data there */
    for (uint32_t i=0; i<data_len-1; i++) {
        if (cam_buf[i] == 0xff && cam_buf[i+1] == 0xd9) {
            ESP_LOGI(TAG, "Shortened data from %d to %d.", data_len, i+2);
            data_len = i+2;
            break;
        }
    }
    ESP_LOGI(TAG, "Picture taken. Size = %d", data_len);

    /* Set some custom headers */
    httpd_resp_set_hdr(req, "Content-Type", "image/jpeg");

    /* Send response with custom headers and body set as the
     * string passed in user context*/
    httpd_resp_send(req, (const char*)cam_buf, data_len);

    cam_give(cam_buf);

    /* After sending the HTTP response the old HTTP request
     * headers are lost. Check if HTTP request headers can be read now. */
    if (httpd_req_get_hdr_value_len(req, "Host") == 0) {
        ESP_LOGI(TAG, "Request headers lost");
    }
    return ESP_OK;
}


