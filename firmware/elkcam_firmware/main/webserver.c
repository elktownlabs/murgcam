/* An HTTP GET handler */
static esp_err_t config_set_handler(httpd_req_t *req)
{
    char content[512];

    /* truncate if content length larger than the buffer */
    size_t recv_size = (req->content_len < sizeof(content)) ? req->content_len : sizeof(content);

    int ret = httpd_req_recv(req, content, recv_size);
    if (ret <= 0) {  /* 0 return value indicates connection closed */
        /* Check if timeout occurred */
        if (ret == HTTPD_SOCK_ERR_TIMEOUT) {
            httpd_resp_send_408(req);
        }
        return ESP_FAIL;
    }

    ESP_LOGI(TAG, "Received set request: %s", content);

    /* parse json */
    cJSON *config_json = cJSON_Parse(content);
    if (config_json == NULL) {
        ESP_LOGE(TAG, "Error parsing json in setconfig post request");
        httpd_resp_send_408(req);
        return ESP_FAIL;
    }

    /* update config storage */
    camera_config_t cam_conf = *(config_cam());
    system_config_t sys_conf = *(config_system());
    cell_config_t cell_conf = *(config_cell());

    cJSON *value;
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cam_quality");
    if (cJSON_IsNumber(value)) {
        ESP_LOGI(TAG, "Setting quality to %d", value->valueint);
        cam_conf.quality = value->valueint;
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cam_auto_exposure");
    if (cJSON_IsNumber(value)) {
        ESP_LOGI(TAG, "Setting auto exposure to %d", value->valueint);
        cam_conf.auto_exposure = value->valueint;
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cam_light_mode");
    if (cJSON_IsNumber(value)) {
        ESP_LOGI(TAG, "Setting light mode to %d", value->valueint);
        cam_conf.light_mode = value->valueint;
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cam_color_saturation");
    if (cJSON_IsNumber(value)) {
        ESP_LOGI(TAG, "Setting saturation to %d", value->valueint);
        cam_conf.color_saturation = value->valueint;
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cam_brightness");
    if (cJSON_IsNumber(value)) {
        ESP_LOGI(TAG, "Setting brightness to %d", value->valueint);
        cam_conf.brightness = value->valueint;
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cam_contrast");
    if (cJSON_IsNumber(value)) {
        ESP_LOGI(TAG, "Setting contrast to %d", value->valueint);
        cam_conf.contrast = value->valueint;
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cell_pin");
    if (cJSON_IsString(value)) {
        ESP_LOGI(TAG, "Setting cell pin to %s", value->valuestring);
        strncpy(cell_conf.pin, value->valuestring, sizeof(cell_conf.pin));
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cell_apn");
    if (cJSON_IsString(value)) {
        ESP_LOGI(TAG, "Setting cell apn to %s", value->valuestring);
        strncpy(cell_conf.apn, value->valuestring, sizeof(cell_conf.apn));
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cell_apn_user");
    if (cJSON_IsString(value)) {
        ESP_LOGI(TAG, "Setting cell apn user to %s", value->valuestring);
        strncpy(cell_conf.apn_user, value->valuestring, sizeof(cell_conf.apn_user));
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cell_apn_pass");
    if (cJSON_IsString(value)) {
        ESP_LOGI(TAG, "Setting cell apn password to %s", value->valuestring);
        strncpy(cell_conf.apn_pass, value->valuestring, sizeof(cell_conf.apn_pass));
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cell_apn_auth");
    if (cJSON_IsNumber(value)) {
        ESP_LOGI(TAG, "Setting apn auth method to %d", value->valueint);
        cell_conf.apn_auth = value->valueint;
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cell_remote_address");
    if (cJSON_IsString(value)) {
        ESP_LOGI(TAG, "Setting remote address to %s", value->valuestring);
        strncpy(cell_conf.remote_address, value->valuestring, sizeof(cell_conf.remote_address));
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "cell_remote_url");
    if (cJSON_IsString(value)) {
        ESP_LOGI(TAG, "Setting remote url to %s", value->valuestring);
        strncpy(cell_conf.remote_url, value->valuestring, sizeof(cell_conf.remote_url));
    }
    value = cJSON_GetObjectItemCaseSensitive(config_json, "sys_secs_between_photos");
    if (cJSON_IsNumber(value)) {
        ESP_LOGI(TAG, "Setting secs between photos to %d", value->valueint);
        sys_conf.secs_between_photos = value->valueint;
    }

    config_write_values(&sys_conf, &cell_conf, &cam_conf);
    ESP_LOGI(TAG, "Saved vaues to storage");
    config_load_values();

    /* send response */
    const char resp[] = "success";
    httpd_resp_send(req, resp, HTTPD_RESP_USE_STRLEN);

    /* After sending the HTTP response the old HTTP request
     * headers are lost. Check if HTTP request headers can be read now. */
    if (httpd_req_get_hdr_value_len(req, "Host") == 0) {
        ESP_LOGI(TAG, "Request headers lost");
    }
    return ESP_OK;
}


